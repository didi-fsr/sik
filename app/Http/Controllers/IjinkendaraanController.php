    {
        //
        $ijinkendaraans = new Ijinkendaraan;


        $ijinkendaraans->id_pd = DB::table('mahasiswa')->where('nm_pd', $request->pemohon)->value('id');
        $ijinkendaraans->merk = $request->merk;
        $ijinkendaraans->tipe = $request->tipe;
        $ijinkendaraans->nomor_plat = $request->nomor_plat;

        $ijinkendaraans->save();
        // Mahasiswa::create($request->all());

        return redirect('/ijinkendaraans');
    }

